/*Cette fonction permet d'écrire un caractère ASCII sur l'écran*/

void tft_char (unsigned int tft_CN,unsigned int tft_PN, char tft_char_disp,char tft_RC,char tft_GC,char tft_BC,char tft_RB,char tft_GB,char tft_BB)
{
    char tft_char_ln;
    char tft_char_bn;
    char tft_char_testb;
        
    tft_Column_Adress_set (8*tft_CN,8*tft_CN+7);
    tft_Page_Adress_set (8*tft_PN,8*tft_PN+7);
    tft_write_mem ();
    for (tft_char_ln=0;tft_char_ln<8;tft_char_ln++)
    {

    for (tft_char_bn=0;tft_char_bn<8; tft_char_bn++)
    {
        tft_char_testb=1<<tft_char_bn;
        if ((tft_char_testb & tft_font8x8 [tft_char_disp][tft_char_ln])==0x00)
        {tft_write_pixel (tft_RB,tft_GB,tft_BB);}
        else
        {tft_write_pixel (tft_RC,tft_GC,tft_BC);}
    }
    }
}

unsigned int TFT_horizontal_pos (void)
{
    int TFT_TP_poshf=0;
    
    _ANSB3 = 1;
    _ANSB15 = 1;
    _ANSB14 = 0;
    _ANSB2 = 0;
    
    _TRISB3 = 1;
    _TRISB15 = 1;
    _TRISB14 = 0;
    _TRISB2 = 0;

    _RB2 = 0;
    _RB14 = 1;
    AD1CHS0 = 0x0003; 
    wait_ms (3);

    _SAMP = 1;
    wait_ms (1);
    _SAMP = 0;
    while (!_DONE);
    
        if (ADC1BUF0 < 63 || ADC1BUF0 > 933)
    {
       TFT_TP_poshf = 320;
    }
    else
    {
       TFT_TP_poshf = (ADC1BUF0 - 63)/2.72;
       TFT_TP_poshf = 319 - TFT_TP_poshf;
    }

    return TFT_TP_poshf;
}

unsigned int TFT_vertical_pos (void)
{
    unsigned int TFT_TP_posvf = 0;
    _ANSB3 = 0;
    _ANSB15 = 0;
    _ANSB14 = 1;
    _ANSB2 = 1;
    
    _TRISB3 = 0;
    _TRISB15 = 0;
    _TRISB14 = 1;
    _TRISB2 = 1;

    _RB3 = 0;
    _RB15 = 1;
    AD1CHS0 = 0x0002; 
    wait_ms (1);

    _SAMP = 1;
    wait_ms (3);
    _SAMP = 0;
    while (!_DONE);
    
        if (ADC1BUF0 < 110 || ADC1BUF0 > 875)
    {
       TFT_TP_posvf = 240;
    }
    else
    {
       TFT_TP_posvf = (ADC1BUF0 - 110)/3.2;
    }

    return TFT_TP_posvf;
}

