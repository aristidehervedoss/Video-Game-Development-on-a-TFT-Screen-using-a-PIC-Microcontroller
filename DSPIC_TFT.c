void tft_texture (char *tab_text,unsigned int tft_C, unsigned int tft_P, unsigned int pix_txt)
{ 
    int tft_num_pix = 0;

    tft_Column_Adress_set (tft_C*pix_txt,tft_C*pix_txt+pix_txt-1);
    tft_Page_Adress_set (tft_P*pix_txt,tft_P*pix_txt+pix_txt-1);
    tft_write_mem ();
    
    for (tft_num_pix=0; tft_num_pix < pix_txt*pix_txt; tft_num_pix++)
    {
        
    TFT_WRX = 0;
    PORTE = tab_text[3*tft_num_pix];
    TFT_WRX = 1;
    TFT_WRX = 0;
    PORTE = tab_text[3*tft_num_pix+1];          /* La fonction tft_write_pixel n'a volontairement par été utilisée pour un souci        */
    TFT_WRX = 1;            /* d'optimisation de vitesse les instruction "GOTO" générées par els appels de fonctions*/
    TFT_WRX = 0;            /* ralentisent fortement la vitesse d'affichage*/
    PORTE = tab_text[3*tft_num_pix+2];
    TFT_WRX = 1;
    
    }

}



